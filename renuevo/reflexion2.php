                    <br>
                    Cuando pienso en los problemas que vienen a nuestra vida y que a veces son tan 
					seguidos, me viene a mi mente Job, ese hombre del cual Dios tenía un muy buen 
					concepto, pero que sin embargo tuvo que pasar por grandes problemas que le 
					llegaron uno tras otro en muy poco tiempo.
					<br>
					<br>
					Job perdió en un solo día, sus bueyes y burros, sus ovejas y sus pastores, sus 
					camellos y sus esclavos y una de las perdidas más dolorosas, mueren sus diez 
					hijos de una sola vez.
					<br>
					<br>
					Luego Job fue víctima de una enfermedad muy difícil, además de esto su mujer se 
					puso en su contra al verlo tirado en su enfermedad, pero me impactan las palabras 
					que Job respondió a su mujer en medio de todas las catástrofes que estaba 
					experimentando: “Pero Job le respondió: —No digas tonterías. Si aceptamos todo 
					lo bueno que Dios nos da, también debemos aceptar lo malo. Y a pesar de todo lo 
					que le había sucedido, Job no pecó contra Dios diciendo algo malo”Job 
					2:10 (Traducción en lenguaje actual).
					<br>
					<br>
					Considero que si cada uno de nosotros tomara muy en serio las palabras de Job, 
					pudiéramos observar la vida desde otro punto de vista, Job dijo: “Si aceptamos 
					todo lo bueno que Dios nos da, también debemos aceptar lo malo” que sabias 
					palabras de un hombre que esta pasando por uno de los mayores problemas que 
					un ser humano puede experimentar.
					<br>
					<br>
					¿Cómo enfrentamos nosotros nuestros problemas?, ¿Qué es lo primero que 
					hacemos?, seamos sinceros, la mayoría de nosotros cuando nos enfrentamos a 
					esos problemas de los que no quisiéramos enfrentar, lo primero que pensamos es: 
					¿Dónde está Dios?, ¿Por qué permite que esto pase?, ¿Por qué a mi, si yo te 
					sirvo?, sin embargo Job decidió aceptar lo malo, así como también le gustaba 
					aceptar lo bueno, WoW sin dudas Job era un hombre tal y como Dios lo había 
					descrito: “Jehová dijo a Satanás: ¿No has considerado a mi siervo Job, que no hay 
					otro como él en la tierra, varón perfecto y recto, temeroso de Dios y apartado del 
					mal?” Job 1:8(Reina-Valera 1960).
					<br>
					<br>
					Job siguió enfrentando muchos más problemas aparte de los que ya existían, 
					perdió su buena reputación delante de los hombres, pero nunca delante de Dios. 
					Sus mejores amigos se apartaron de él dejándolo solo en su momento de crisis. 
					Pero me es motivo de ejemplo lo que Job contesta en medio de su momento de 
					angustia: “Yo sé que mi Redentor vive…” Job 19:25a(Reina-Valera 1960). Creer 
