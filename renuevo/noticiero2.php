                    <p id="parrafos">
					
					<img src="images/noticia2.jpg" class="img-alineo" align="left"><b>
					SANTO DOMINGO, República Dominicana.- Defensores de la comunidad de lesbianas, gais, bisexuales, 
					transexuales e intersexuales (LGBTI) reclamaron a la Organización de Estados Americanos (OEA) este
					lunes 13, acciones firmes para erradicar los actos de discriminación a los miembros del referido colectivo. </b>
					<br>
					<br>
					<br>
					<br>
					<br>
					<br>
                    Camila Sabala, representante de la coalición de la LGBTTTI de las Américas, reiteró la exigencia a la OEA 
					para que firmen y ratifiquen los tratados contra toda forma de discriminación, racismo y defensa de adultos
					mayores, que ya fueron aprobados.
					<br>
					<br>
					“Es un compromiso y una deuda que tienen todos los países de América con todas las personas diversas”, señaló Sabala.
					<br>
					<br>
					La activista, quien se presentó frente al salón de conferencias del Ministerio de Relaciones Exteriores (MIREX),
					junto a otros representantes de grupos en riesgo de exclusión, indicó que los crímenes de odio, en referencia 
					al asesinato de 50 personas la madrugada del domingo 12 en Orlando, Estados Unidos, en contra de personas LGBTI
					ocurren de manera frecuente en los países de la región, lo cual se debe, principalmente, a los mensajes de odio
					que transmiten algunos sectores religiosos y más conservadores.
					<br>
					<br>
					De su lado, Chantal Cuellar, secretaria nacional de la Red Trebol, lamentó que la población trans, el cual representa
					el sector más vulnerable dentro del colectivo LGBTI, se mantenga excluida dentro de la sociedad, las cuales en 
					numerosos casos, tienen restringido el acceso a salud, educación y trabajo seguro.
					<br>
					<br>
					“Los estados no se están comprometiendo a legislar políticas públicas en favor de nuestro sector, como una ley de 
					identidad de género para ejercer nuestra ciudadanía. Nos están matando. El mismo Estado nos discrimina”, aseveró.
					<br>
					<br>
					Expuso que Bolivia sirve como un ejemplo para las naciones vecinas, debido a que aprobó una ley de identidad de 
					género, con la cual las personas trans pueden exigir sus derechos.
					<br>
					<br>
					En el caso de República Dominicana, Casandra Peralta, representante de Cotrave, indicó que en el país, las 
					personas trans no tienen acceso a un sistema de salud estable.
					<br>
					<br>
					“Queremos que la igualdad sea para todos. Buscamos derechos de igualdad”, resaltó Peralta. 
					<br>
					<br>
					Sin embargo, dijo entender que la nación ha dado pasos de avance en el reconocimiento de los derechos del colectivo,
					sin embargo, sostuvo que aún queda un largo camino por recorrer. “Estamos en pie de lucha”.
					<br>
					<br>
					Cristian Ramírez, representante de Revasa, indicó que el nivel de violencia y discriminación continúa latente, 
					contabilizándose 30 homicidios de personas gais a nivel nacional. De estos, 26 fueron de personas trans. Solo dos 
					de dichos casos fueron ventilados en la justicia, lográndose la condena de uno de ellos.
					<br>
					<br>
					"Eso refleja mucho de cómo está la justicia dominicana en tema LGBT”, indicó. “No se ve como un crimen de odio, 
					sino como un crimen más, pero el crimen de odio tiene otra connotación”, explicó, dado que dichos crímenes son 
					por una convicción personal sobre lo que es “bueno o malo”.
					<br>
					<br>
					“No se trata de lo que es bueno o es malo, es una cuestión de derechos. Eso es lo que procuramos”, sostuvo Ramírez.
					<br>
					<br>
					De su lado, el regidor y excandidato a diputado de la alianza del Partido Revolucionario Moderno (PRM) y el Movimiento C+,
					lamentó que en lo que va de año, unos 500 hombres y mujeres gais, lesbianas y transexuales han muerto en el continente 
					americano, entre ellas el activista y político hondureño Reni Martínez.
